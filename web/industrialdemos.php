<h2>What to Expect?</h2>

<p>Accepted demos will be scheduled in a two-hour session at the conference, during which the demo will need to be manned. Demos can stay up during the entire day, so it is possible to show it to participants during breaks as well. Demos that utilize wireless networks must be prepared to arrange appropriate spectrum sharing with other such demos at the venue. Demos that require large amounts of Internet uplink capacity are unlikely to find that available at the venue.</p>

