<h1>Call for Industrial Demos</h1>

<p>The industrial demo sessions at SIGCOMM 2014 and 2015 had a great turnout and positive feedback, so it returns for SIGCOMM 2016. The industrial demo session showcases demos from industry in the area of networking, networked systems, and networked applications. The objective of this session is to bring together industry and academia around demos of innovative and cutting-edge networking products.</p>

<h2> Why Present Industrial Demos at SIGCOMM?</h2>

<p>SIGCOMM is the flagship annual conference of the ACM Special Interest Group on Data Communication (SIGCOMM) on the applications, technologies, architectures, and protocols for computer communication. The conference is single-track and features a high-quality technical program with significant opportunities for technical and social interactions. SIGCOMM is the premier conference in its field and typically attracts over 600 attendees (scientists, engineers, educators, and students).</p>

<p>An industrial demo is an opportunity for companies to show industrial leadership. Presenting edge industrial technology will attract top academics to work on problems that are relevant to industry and help push forward the frontier of industrial knowledge. The industrial demo session is also a great recruiting opportunity. SIGCOMM attracts students from the most selective universities around the world.</p>

<h2>How to Submit?</h2>

<p>Please submit a small description of your demo (up to one page) together with the names and affiliation of the demo presenters at <a href="https://sigcomm-industrialdemos16.hotcrp.com/">https://sigcomm-industrialdemos16.hotcrp.com/</a>. The SIGCOMM industrial liaison board is responsible for the selection of industrial demo program.</p>

<p>Demos of research projects from industry are better suited for the general SIGCOMM demo session; the industrial demo session has more of a product focus. Feel free to contact any member of the SIGCOMM industrial liaison board or the demo chairs if you have any questions.</p>

