<h2>Where and How to Apply</h2>

<p>All application materials (applicant's info, CV, and letters) should be submitted 
    electronically to the following website: 
    <i><b>submission site that will be online soon</b></i>.
</p>

<p>The applicant should create an account in the submission system and upload:
    <ol>
        <li>an up-to-date CV</li>
        <li>an application letter, and</li>
        <li>a letter from their supervisor (Ph.D. adviser for students, Ph.D or postdoc adviser for postdocs, or supervisor for faculty/researchers).</li>
    </ol>
</p>

<p>
The applicant's letter should be clearly divided in the following sections:
    <ol>
        <li>an explicit identification of (a) the applicant's current country of study or work, (b) academic status (student, postdoc, or junior faculty member), and (c) whether the applicant qualifies for a GeoDiversity travel grant (see above);</li>
        <li>whether the applicant is the author of a workshop paper or poster; and whether the applicant will participate in the ACM Student Research Competition (SRC)</li>
        <li>a brief summary of where the applicant is studying/working (provide a URL for a home page if one exists) and who the applicant's advisor/supervisor is; any additional information the applicant feels is relevant to support their case;</li>
        <li>a brief summary of research interests and plans for future research; and</li>
        <li>why the conference attendance is important to the applicant's research</li>
        <li>a cost estimate of the trip, listing the cost of airfare, hotel, and the source of any price quote.</li>
    </ol>
</p>

<p>
The supervisor's letter should be clearly divided in the following sections:
    <ol>
        <li>the applicant's good standing in the institution;</li>
        <li>explain why the applicant would benefit from attending SIGCOMM as well as how he/she could contribute to the conference; and</li>
        <li>explain the current funding status and why the applicant is in need of the travel grant.</li>
    </ol>
</p>
<p>
    Supervisor letters should be uploaded directly into the applicant's account. 
    We suggest that the applicant provide their login information to the supervisor 
    to do so. The submission site has been designed to keep supervisor letters 
    "write-only", i.e., the applicant will not be able to view the uploaded letter 
    in their account.
</p>

