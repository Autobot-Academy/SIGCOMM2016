<h2>Where and How to Apply</h2>

<p>All application materials (applicant&#39;s info, CV, and letters) should be submitted electronically to the following website: <a href="https://sigcomm16travel.hotcrp.com/" rel="external">https://sigcomm16travel.hotcrp.com/</a>.
</p>

<p>The applicant should create an account in the submission system and upload:
    <ol>
        <li>An up-to-date CV</li>
        <li>An application letter, and</li>
        <li>A letter from their supervisor (Ph.D. adviser for students, Ph.D or postdoc adviser for postdocs, or supervisor for faculty/researchers). The recommendation letter can be directly uploaded by the applicant during the application process. Alternatively, the advisor can directly email the recommendation letter to Sue Moon (<a href="mailto:sbmoon@kaist.edu">sbmoon@kaist.edu</a>) if confidentiality is a concern.</li>
    </ol>
</p>

<p>
The applicant&#39;s letter should be clearly divided in the following sections:
    <ol>
        <li>An explicit identification of (a) the applicant&#39;s current country of study or work, (b) academic status (student, postdoc, or junior faculty member), and (c) whether the applicant qualifies for a GeoDiversity travel grant (see above);</li>
        <li>Whether the applicant is the author of a workshop paper or poster; and whether the applicant will participate in the ACM Student Research Competition (SRC)</li>
        <li>A brief summary of where the applicant is studying/working (provide a URL for a home page if one exists) and who the applicant&#39;s advisor/supervisor is; any additional information the applicant feels is relevant to support their case;</li>
        <li>A brief summary of research interests and plans for future research; and</li>
        <li>Why the conference attendance is important to the applicant&#39;s research</li>
        <li>A cost estimate of the trip, listing the cost of airfare, hotel, and the source of any price quote.</li>
    </ol>
</p>

<p>
The supervisor&#39;s letter should be clearly divided in the following sections:
    <ol>
        <li>The applicant&#39;s good standing in the institution;</li>
        <li>Explain why the applicant would benefit from attending SIGCOMM as well as how he/she could contribute to the conference; and</li>
        <li>Explain the current funding status and why the applicant is in need of the travel grant.</li>
    </ol>
</p>

