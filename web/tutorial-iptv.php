<h2>Summary</h2>

<p>This tutorial consists of two main parts. In the first part, we provide a detailed overview of
IPTV and its building blocks, explain the architectures and protocols used to carry video 
over IP in core, aggregation, access and home networks. We discuss the experiences from 
the existing deployments, and present the relevant standards and current challenges.</p>

<p>In the second part, we survey well-established streaming solutions for over-the-top (OTT) 
video delivery, explaining how OTT video delivery contrasts to traditional broadcast and 
managed IPTV services. We then describe existing and emerging service models reliant 
upon Internet video, including HTTP adaptive streaming along with all the workflows for 
content generation, distribution and consumption. Throughout this part, we review recent 
research findings and discuss future directions. Emerging standards and industry practices 
will be covered as well.</p>

<p>Upon attending this tutorial, the participants will have an understanding of the following:</p>

<ul>
  <li>Fundamental differences between IPTV and IP video</li>
  <li>Video transport, protocols and standards for IPTV</li>
  <li>Features of various types of streaming protocols</li>
  <li>Principles of HTTP adaptive streaming</li>
  <li>Content generation, distribution and consumption workflows</li>
  <li>Standards and emerging technologies in IP video space</li>
  <li>Current and future research on managed and unmanaged video delivery</li>
</ul>

<h2>Motivation</h2>

<p>Video over IP is a reality to all of us and its growth pace is unprecedented. This is an area 
that cannot be ignored by anybody who is working in the networking field. Unlike most of 
its counterparts that offer mostly theoretic content, this tutorial will focus on practical 
matters such as existing products and solutions that are being used by millions of users 
around the globe and the new developing technologies that will be used by the same users 
in a few years.</p>

