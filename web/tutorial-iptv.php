<h2>Biography</h2>

<p><b>Ali C. Begen</b> has joined MediaMelon, Inc. as the principal architect for streaming 
technologies in February 2016, where he is currently heading the development efforts for 
MediaMelon&#39;s content-aware streaming solutions that enable premium viewer experience 
while minimizing operational costs and improving profitability. He has been a research and 
development engineer since 2001, and has broad experience in mathematical modeling, 
performance analysis, optimization, standards development and intellectual property 
innovation. Between 2007 and 2015, he was with the Video and Content Platforms 
Research and Advanced Development Group at Cisco, where he has architected, designed 
and developed algorithms, protocols, products and solutions in the service provider and 
enterprise video domains. Since 2007, he has been leading professional and academic 
projects, and teaching graduate-level courses and giving lectures in universities around the 
world.</p>

<p>Ali holds a Ph.D. degree in electrical and computer engineering from Georgia Tech. He 
received a number of scholar and industry awards, and he has editorial positions in 
prestigious magazines and journals in the field. He is a senior member of the IEEE and a 
senior member of the ACM. More recently, in January 2016, he was elected as a 
distinguished lecturer by the IEEE Communications Society. Further information on his 
projects, publications, keynotes, tutorials, and teaching, standards and professional 
activities can be found at <a href="http://ali.begen.net" rel="external">http://ali.begen.net</a>.</p>

