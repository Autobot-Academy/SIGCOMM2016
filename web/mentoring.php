<h1>Mentoring Moments at ACM SIGCOMM 2016</h1>

<p>ACM SIGCOMM 2016 will offer the opportunity for what we call &quot;mentoring moments.&quot; The idea is to give graduate students a chance to talk one-on-one (or, in some cases, one-on-two) about their research with outstanding researchers beyond those available at the students&#39; universities.</p>

<p>A graduate student can apply for a private meeting with one or two well-known researchers to get feedback on a research idea. The idea can be a dissertation proposal or simply a research idea that the student is currently pursuing.</p>

<p>The mentoring sessions were held for the first time at SIGCOMM&#39;15 (<a href="http://conferences.sigcomm.org/sigcomm/2015/mentoring.php" rel="external">http://conferences.sigcomm.org/sigcomm/2015/mentoring.php</a>). For SIGCOMM&#39;16, we have made some adjustments to the process (see below).</p>

<h2>The Process</h2>

<p>This year we are planning to have two types of mentoring sessions:</p>

<p><b>Formal</b>: The graduate student gets 25 minutes in which to explain her or his work to a team of two mentors. The presentation can be about work-in-progress or a possible dissertation thesis idea or any research topic in networking that the student wants to explore. Another 15 minutes are reserved for general discussion and feedback from the mentors. (So the session is a total of 40 minutes). Students are encouraged to bring slides to explain their ideas, but this meeting is not a talk. It is a candid exchange, in which the student presents their ideas and goals and the researchers offer constructive feedback.</p>

<p>The student&#39;s advisor is encouraged to attend, but should let the student and mentors lead the session.</p>

<p><b>Informal</b>: The graduate student will be paired with a mentor. We will arrange a one-to-one meeting between the student and mentor during the welcome reception or coffee breaks (the meeting will typically last 15-20 minutes). Students should view this as an opportunity for a structured hallway discussion with the mentor -- they can get advice about the positioning, direction, or any other aspect of their work.</p>

<h2>How to Apply</h2>

<p>For full consideration, please apply by August 1, 2016 using this form: <a href="https://goo.gl/TLBlEq" rel="external">https://goo.gl/TLBlEq</a>.</p>

<p>We will evaluate your application to make sure it is in scope for our SIGCOMM mentors and then seek to assign you a time with mentor(s) with relevant expertise in your topic area.</p>

<p>Students are welcome to indicate their preference about the kind of mentoring (formal or informal) they would like to receive. We will make the final decision based on your preference, availability of mentors, and our own judgement regarding which type of mentoring will best serve your needs.</p>

<p>If we are oversubscribed for mentoring sessions, we&#39;ll do some mix of trying to sign up more mentoring teams, finding more space for mentoring meetings, and, if we must, trying to figure out who will most benefit from the mentoring sessions.</p>

<h2>When Sessions Will Take Place</h2>

<p>Sessions will be scattered through the conference week.</p>

