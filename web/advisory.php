<h1>Travel Advisory Notes</h1>

<p>With regard to the recently developments reported in the media about the risks associated with Zika virus in Brazil, we would like to provide prospective SIGCOMM'16 attendees with information about the disease, infection, risks, and actions being taken by local authorities to reduce its spreading (and also of other related diseases).</p>

<p>For <b>U.S. CDC</b> travel information about Zika, please <a href="http://wwwnc.cdc.gov/travel/page/zika-travel-information">click here</a>.</p>

<p>For <b>World Health Organization</b> fact sheet about Zika, please <a href="http://www.who.int/mediacentre/factsheets/zika/en/">click here</a>.</p>

<p>More recently, Brazilian Ambassador to the U.S. <i>Luiz Alberto Figueiredo Machado</i> <a href="http://americasquarterly.org/content/brazil-doing-its-part-global-fight-against-zika">wrote a news article</a> about how local authorities are dealing with Zika outbreak. The Brazilian public news agency E.B.C. also released a <a href="http://agenciabrasil.ebc.com.br/en/geral/noticia/2016-01/brazil-launches-clean-campaign-against-aedes-aegypti">news statement</a> about actions being taken to combat Aedes Aegypti, which transmits the viruses of Dengue fever, Chikungunya, and Zika.</p>

