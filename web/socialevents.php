  <li><b>Desserts</b> &mdash; Special selection of mini-mousses, offered in the following flavors: Chocolate, Soursop, Passion fruit, and Sicilian lime.</li>
  <li><b>Drink Selection</b> &mdash; Beers, soft drinks, and &quot;caipirinhas&quot; made of limes and Brazilian rum (&quot;cacha&ccedil;a&quot;) or vodka.</li>
</ul>

