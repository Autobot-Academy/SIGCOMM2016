<a name="conf_banquet"></a><h2>Conference Banquet, Tuesday 23rd</h2>

<p>The Conference Banquet will take place at the Slaviero Essential Florian&oacute;polis Ingleses (Rua das Gaivotas 1114, Praia dos Ingleses District &mdash; <b>see location on the map below</b>).</p>

<p>The banquet menu shall include:

<ul>
  <li><b>Oyster Bar</b> &mdash; Fresh Baby Oysters accompanied with Lambrusco Italian wine, Mandarin and Sicilian limes, Brazilian rum (also known as &quot;cacha&ccedil;a&quot;), selected Olive Oils, and Tabasco sauce.</li>
  <li><b>Entre&eacute;s</b> &mdash; Crab meat in the shell; Frescal Shepherd&#39;s pie (Frescal is a dried meat produced in the highland region of Santa Catarina); Shrimp Bob&oacute; (shrimp in a pur&eacute;e of manioc meal, coconut milk, and other ingredients); Beans Soup (accompanied with bacon and chives).</li>
  <li><b>Salads</b> &mdash; Special selection Green salad (accompanied with Gran Mestri cheese and seasoned with raspberry sauce); Smoked Poultry salad; and Styllu&#39;s salad (tropical fruits seasoned with yogurt and mint sauce).
