<h3>By Taxi</h3>

Taxi fare from Hercilio Luz In&#39;l Airport to Ingleses District is estimated (approximations only) to R$100 - R$120. The fare from Ingleses District back to the Airport is R$110 - R$135. Observe that fares may change depending on the time of the day, weekday (and traffic jams). For a reference, see <a href="http://j.mp/1YoykQC">http://j.mp/1YoykQC</a>.

