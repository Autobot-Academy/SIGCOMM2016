<h1>Visa Information</h1>

<p>For international registrants, a <a href="http://boston.itamaraty.gov.br/en-us/eligibilities.xml" target="sigcomm_visa_info" rel="external">tourist visa</a> suffices to attend scientific conferences (provided that no wages are received). Please note that the Brazilian visa system operates under a strict reciprocity policy. It means that citizens from countries
which require a visa from Brazilians will have to obtain a visa to attend the conference (there are a few
temporary exceptions, see further).</p>

<p>Visitors to Brazil are generally required to hold
a passport having 2 blank pages, and that will be valid for six months past the date of first entry.
Visitors should also pay special attention to
<a data-ajax="false" href="#supp_docs">supporting documents</a> typically asked during immigration.</p>

<p>With regard to country-specific visa requirements, we organize visitors in three main categories:

<ul>
  <li><a data-ajax="false" href="#cat_a">Countries with permanent visa waiver</a></li>
  <li><a data-ajax="false" href="#cat_b">Countries with temporary tourism visa waiver (Summer Olympic Games)</a></li>
  <li><a data-ajax="false" href="#cat_c">Countries without visa waiver</a></li>
</ul>
</p>
<h2><a name="cat_a"></a>A. Countries with Permanent Visa Waiver</h2>

<p>Nationals from <a href="http://atlanta.itamaraty.gov.br/en-us/visa_requirements_by_country.xml" target="sigcomm_visa_info" rel="external">several countries</a> (for example, the whole European Union and most of Latin 
America) are waived from having a visa to enter Brazil, and may stay for 30 to 180 days within a six or twelve months period,
depending on nationality. Examples are Great Britain (UK), Germany, Sweden, France, South Korea, Israel, Russia, New Zealand, Argentina, and Uruguay.</p>

<p>During immigration, you may be asked to present proof of registration and a <a data-ajax="false" href="#supp_letter">letter of invitation</a> issued by conference organizers, in addition to other <a data-ajax="false" href="#supp_docs">supporting documents</a>.</p>

<h2><a name="cat_b"></a>B. Countries with Temporary Tourism Visa Waiver</h2>

In order to foster tourism during the Summer Olympic Games 2016, the Brazilian government
<a href="https://www.rio2016.com/en/news/copacabana-calling-brazilian-government-extends-visa-waiver-for-rio-2016-olympic-and-paralympic-games" target="sigcomm_visa_info" rel="external">unilaterally waived</a> visa requirements for some countries. The waiver is valid for nationals from
<b>U.S.</b>, <b>Australia</b>, <b>Canada</b>, and <b>Japan</b> only, when traveling to Brazil for tourism (under a tourist visa)
between June 1st and September 18th (therefore including SIGCOMM).
The maximum period of stay is 90 days after first entry, non-extendable. Nationals from these four countries will not be
required to hold tickets for the Olympic Games to benefit from the visa waiver.
If in doubt, please consult the nearest <a href="http://www.portalconsular.mre.gov.br/sites-dos-postos/por-ordem-alfabetica" rel="external" target="sigcomm_visa_info" >Brazilian Consulate</a> to find out whether a visa is required in your case.

<h2><a name="cat_c"></a>C. Countries Without Visa Waiver</h2>

<p>For nationals of countries without visa waiver agreements with Brazil, neither permanent nor
temporary, a <a href="http://boston.itamaraty.gov.br/en-us/vitur_-_tourist_visa.xml" target="sigcomm_visa_info" rel="external">tourist visa</a> is required. Examples are China, India, and Pakistan. We refer to the <a href="http://boston.itamaraty.gov.br/en-us/vitur_-_tourist_visa.xml" rel="external" target="sigcomm_visa_info">Consulate General of Brazil in Boston</a> for
an overview about how to get a visa to Brazil.</p>

<p>The process to obtain a visa requires a <a data-ajax="false" href="#supp_letter">letter of invitation</a> issued by conference organizers. Please see below how to obtain one.</p>

<p>Please note that each Brazilian Consulate has its own visa processing workflow. For specific
document requirements, visa application forms, fees and payment methods, please contact your
country&#39;s <a href="http://www.portalconsular.mre.gov.br/sites-dos-postos/por-ordem-alfabetica" rel="external" target="sigcomm_visa_info" >diplomatic representation</a> (in Portuguese, sorted by country). International registrants
should be particularly aware and careful about procedures and timelines (among others), and
should plan travel well in advance. Depending on the country, there are companies specialized in
assisting visitors in the process of obtaining a visa.</p>

<h2><a name="supp_docs"></a>General Advice for International Registrants</h2>

<p><b>Before departing to Brazil</b>, we recommend special attention to <a href="http://atlanta.itamaraty.gov.br/en-us/visa_requirements_by_country.xml" rel="external" target="sigcomm_visa_info">certain documents</a> typically asked during immigration, in addition to passport (with a visa, if applicable) &mdash; proof of funds for your stay, hotel reservation, and return
ticket or airline itinerary. The list may vary according to nationality, and at the discretion of the Immigration Officer.</p>

<ul>
  <li><b>Vaccination Requirements.</b> A polio vaccination certificate <a href="http://boston.itamaraty.gov.br/en-us/faq.xml" rel="external" target="sigcomm_visa_info">is compulsory</a> for children between 3 months and 6 years of age. A yellow fever international certificate of vaccination or prophylaxis is not mandatory to enter Brazil, but may be required for some nationals upon re-entry to their countries (for example, <a href="http://cingapura.itamaraty.gov.br/en-us/yellow_fever_requirements.xml" rel="external" target="sigcomm_visa_info">Singapore</a>).<br/><br/></li>
  <li><b>Entry and Exit for Minors.</b> For anyone under 18, see <a href="http://boston.itamaraty.gov.br/en-us/visa_for_minors.xml" rel="external" target="sigcomm_visa_info">specific requirements</a> regarding birth certificates and notarized travel authorization.<br/><br/></li>
  <li><b>Nationals with Brazilian Citizenship.</b> Brazilian citizens, even those with dual citizenship, must enter Brazil with their Brazilian passport.</li>
</ul>

<h2><a name="supp_letter"></a>Letter of Invitation</h2>

<p>Nationals that fall in category C (without waiver agreement) are typically required to include an Invitation Letter as part of the visa application process. The letter can also be used for the admission of nationals in category A, though it is not mandatory.</p>

<p><font color="#ff0000"><b>Important update:</b></font> To request a letter, first register for the conference, and then fill in the <a href="http://goo.gl/forms/ZBMWiDwaPLrrT8Bo2">invitation letter request form</a>. Please include

<ol>
<li>Your name as it appears on your passport</li>
<li>Your current postal mailing address</li>
<li>The name of the conference you wish to attend (&#8220;ACM SIGCOMM 2016&#8221;)</li>
<li>Your Registration Confirmation Number</li>
<li>If you have any papers accepted for the conference, please provide the title and indicate whether you are the &#8220;sole author&#8221; or a &#8220;co-author&#8221;</li>
</ol>
</p>

<p>Visa support letters are answered in the order they are received. You will receive a PDF letter by email within 4-5 business days. Please note we do not fax visa support letters. The PDF serves as a signed electronic version of the original letter which will be mailed to your current mailing address. If you do not receive the letter by post mail on time for your visa appointment, please print out your PDF letter and take it with you.</p>

<p>Please note that the letter of invitation will only be sent to participants
who have registered and paid for the conference. Should participants
withdraw from the conference after the issue of a letter of invitation,
the consulate will be notified of the withdrawal accordingly.</p>

