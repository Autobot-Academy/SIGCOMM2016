<h1>Call for Posters, Demos, and Student Research Competition</h1>

<p>The SIGCOMM poster and demo sessions showcase works-in-progress in an informal setting. Topics of interest are the same as research topics in the SIGCOMM conference call for papers. We strongly encourage <b>student</b> and <b>industry</b> submissions. The SIGCOMM 2016 Poster and Demo committee will review all posters and demo proposals. Students must present student posters at the conference. Authors of accepted papers in SIGCOMM 2016 may not submit a poster on the same work as in the paper.</p>

<p>Product-focused, as opposed to research-focused, industrial demos may be more appropriately submitted to the SIGCOMM Industrial Demo track. Please feel free to contact either the Poster and Demo Chairs or the <a href="http://www.sigcomm.org/content/sigcomm-industrial-advisory-board">SIGCOMM Industrial Liaison Board</a> if you are unsure which track to submit to.</p>

<h3>ACM Student Research Competition (SRC)</h3>

<p>The SIGCOMM poster session will also serve as an <a href="http://src.acm.org/index.html">ACM Student Research Competition</a>. Qualified entrants must have current ACM student membership, have graduate or undergraduate student status at the time of submission (May 2016), and be the only student working on the project. If a graduate student is part of a group research project and wishes to participate in an SRC, they can submit and present their individual contribution to the group research project.</p>

<p>While not mandatory, entrants are encouraged to submit a letter from their advisor describing the specific contributions made by the student. Undergraduates and graduate students will be treated in separate divisions (students starting their first year of graduate school at the time of the conference will be considered as undergraduates). A small <a href="http://src.acm.org/students.html">travel supplement</a> is made available to accepted SRC entrants; please also submit applications for travel grant support. Winners will advance to ACM Grand Finals of the Student Research Competition to compete against the winners of other ACM conferences. The SRC is sponsored by Microsoft Research.</p>

<h3>Why should you submit a Poster or a Demo?</h3>

<p>Presenting a poster is a great opportunity, especially for students, to obtain interesting and valuable feedback on ongoing research from a knowledgeable crowd at the conference. Accepted posters and demos will be published as a two-page abstract for the archived conference proceedings. In addition, the top few submissions will be forwarded for publication to the SIGCOMM newsletter, the Computer Communication Review (CCR). Students who are submitting posters are highly encouraged to examine if they are eligible for student travel grants.</p>

<h3>What is a Poster?</h3>

<p>We expect both poster and demo presenters to prepare a poster. A poster is A0 paper size in portrait mode (841 x 1189mm), to which you can affix visually appealing material that describes your research. Alternatively, you can use the space as a continuum. You should prepare the best material (visually appealing and succinct) that effectively communicates your research problem, techniques, results, and what is novel/important about the work. You do not submit such a large-format image; only an abstract describing in text what the poster would present. The abstract should clearly state: (a) the problem being addressed, (b) what makes this problem interesting, important, and difficult, (c) your approach to the problem, and (d) the key contribution.</p>

<h3>What To Submit</h3>

<p>If you are submitting a poster, you must submit a two-page abstract in PDF format that describes your work.</p>

<p>If you are submitting a demo, you must submit a three-page abstract (two page overview, one page demo requirements). The third page of technical requirements should detail:</p>

<ul>
<li>Equipment to be used for the demo</li>
<li>Space needed</li>
<li>Setup time required</li>
<li>Additional facilities needed, including power and any Internet access requirements</li>
</ul>

<p>All demos will be provided with table space, a poster board and wireless Internet access by default.</p>

<p>In both cases, acceptance will be primarily based on review of the submitted abstract.</p>

<p>Prepare your abstract using <a href="http://www.acm.org/sigs/publications/proceedings-templates">ACM SIG Alternate conference style</a>. Word documents will not be accepted. The abstracts of accepted posters and demos will be available to all attendees at the conference.</p>

<p>It is highly encouraged that each <b>demo</b> proposal includes a video clip showcasing the work, in addition to the abstract. The video should be no more than 3 minutes and should give a good idea of what the demo is about and what it would look like. Including a video clip will help the committee better understand and evaluate your proposal.</p>

<p>Authors of accepted demos and posters will be encouraged to publish auxiliary material in the ACM Digital Library with their poster/demo (source code, packet traces, and so forth) to improve the reproducibility of their results. The auxiliary material does not need to be submitted but can be referenced in the submission.</p>

<p>The posters and demos submitted to SIGCOMM 2016 must be original and cannot be concurrently submitted to other workshops or conferences during the SIGCOMM poster/demo review period. Dual submissions are a waste of the reviewer&#39;s time.</p>

<h3>Where To Submit</h3>

<p>Please submit your abstract at <a href="https://sigcomm16posters.hotcrp.com/">https://sigcomm16posters.hotcrp.com/</a>. Submissions are single blind, so please include authors&#39; names and affiliation. When submitting a poster, indicate if the submission should be considered for the SRC.</p>

